public class twodarrays {

    public static void main(String[] args) {


        String[][] cars = new String[3][3];
        cars[0][0] = "corveto";
        cars[1][0] = "fifacar";




        for (int i = 0; i < cars.length; i++) {
            System.out.println();
            for (int j = 0; j < cars[i].length; j++) {
                System.out.print(cars[i][j]+" ");


            }


        }


    }
}



